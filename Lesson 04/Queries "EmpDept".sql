/*Query 07*/
SELECT DEPTNO
FROM DEPT
MINUS
SELECT DEPTNO
FROM EMP
GROUP BY DEPTNO;

/*Query 08*/
SELECT *
FROM EMP
WHERE JOB =
      (SELECT JOB
       FROM EMP
       WHERE ENAME = 'JONES');

/*Query 09*/
SELECT *
FROM EMP
WHERE SAL >
      (SELECT MIN(SAL)
       FROM EMP
       WHERE DEPTNO = 30);

/*Query 10*/
SELECT *
FROM EMP
WHERE SAL >
      (SELECT MAX(SAL)
       FROM EMP
       WHERE DEPTNO = 30);

/*Query 11*/
SELECT *
FROM EMP
WHERE DEPTNO = 10
  AND JOB NOT IN
      (SELECT JOB
       FROM EMP
       WHERE DEPTNO = 30);

/*Query 12*/
SELECT EMPNO, ENAME, JOB, SAL
FROM EMP
WHERE SAL =
      (SELECT MAX(SAL)
       FROM EMP);