SELECT *
FROM EMP
WHERE COMM > SAL;

/*Query 10*/
SELECT *
FROM EMP
WHERE DEPTNO = 30
  AND SAL >= 1500;

/*Query 11*/
SELECT MGR
FROM EMP
WHERE DEPTNO != 30;

/*Query 12*/
SELECT *
FROM EMP
WHERE DEPTNO = 10
  AND JOB != 'CLERK'
  AND JOB != 'MANAGER';

/*Query 13*/
SELECT *
FROM EMP
WHERE SAL
          BETWEEN 1200
          AND 1300;

/*Query 14*/
SELECT *
FROM EMP
WHERE ENAME
          LIKE 'ALL__';

/*Query 15*/
SELECT SAL + COMM
FROM EMP
WHERE SAL + COMM IS NOT NULL;

/*Query 16*/
SELECT *
FROM EMP
WHERE COMM > SAL * 0.25;

/*Query 17*/
SELECT AVG(SAL)
FROM EMP
WHERE JOB != 'PRESIDENT';

/*Query 18*/
SELECT COUNT(*)
FROM EMP
WHERE COMM IS NOT NULL
  AND COMM > 0;

/*Query 19*/
SELECT COUNT(DISTINCT JOB)
FROM EMP
WHERE DEPTNO = 30
GROUP BY DEPTNO;

